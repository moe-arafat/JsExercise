function add(a, b){
    return a + b;
}

function subtract(a, b){
    return a - b;
}

function multiply(a, b){
    return a * b;
}

function divide(a, b){
    return a / b;
}

console.log("Addition: " + add(10, 7));
console.log("Subtraction: " + subtract(20, 10));
console.log("Multiply: " + multiply(10, 9));
console.log("Division: " + divide(25, 5));
console.log("Division by zero: " + divide(10, 0));